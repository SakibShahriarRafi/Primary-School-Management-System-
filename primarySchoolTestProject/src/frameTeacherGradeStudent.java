    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int inputclass = Integer.parseInt(tfClass.getText());
        String section = tfSection.getText();
        int roll = Integer.parseInt(tfRoll.getText());
        String courseid = tfCourseid.getText();
        double obtainedMark = Double.valueOf(tfObtainedMarks.getText());
        String obtainedGrade =null;
        int isPassed =1;
        
        if (obtainedMark >= 90) {
            obtainedGrade = "A+";
        } else if (obtainedMark >= 80) {
            obtainedGrade = "A";
        } else if (obtainedMark >= 70) {
            obtainedGrade = "B";
        } else if (obtainedMark >= 60) {
            obtainedGrade = "C";
        } else if (obtainedMark >= 50) {
            obtainedGrade = "D";
        } else {
            obtainedGrade = "F";
        }
        if (obtainedGrade.equalsIgnoreCase("F")) {
            isPassed= 0;
        }
        
        String jdbcUrl = "jdbc:sqlserver://localhost:1433;databaseName=RainbowPrimarySchool";
        String username = "sa";
        String password = "123456";
        
        try (Connection connection = DriverManager.getConnection(jdbcUrl, username, password)) {
            // Query the StudentTable to get the corresponding studentId
            String sqlSelect = "SELECT studentId FROM StudentTable WHERE studentCurentClass = ? AND studentSection = ? AND StudentCurrentRoll = ?";
            try (PreparedStatement preparedStatement = connection.prepareStatement(sqlSelect)) {
                preparedStatement.setInt(1, inputclass);
                preparedStatement.setString(2, section);
                preparedStatement.setInt(3, roll);

                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    // Check if the student exists in the StudentTable
                    if (resultSet.next()) {
                        int studentId = resultSet.getInt("studentId");

                        // Insert the data into the GradeTable
                        String sqlInsert = "INSERT INTO GradeTable VALUES (?, ?, ?,?, ?, ?,?, ?, ?)";
                        try (PreparedStatement insertStatement = connection.prepareStatement(sqlInsert)) {
                            insertStatement.setInt(1, studentId);
                            insertStatement.setInt(2, teacherId);
                            insertStatement.setString(3, courseid);
                            insertStatement.setInt(4, inputclass);
                            insertStatement.setString(5, section);
                            insertStatement.setInt(6, roll);
                            insertStatement.setDouble(7, obtainedMark);
                            insertStatement.setString(8, obtainedGrade);
                            insertStatement.setInt(9, isPassed);
                            

                            int rowsInserted = insertStatement.executeUpdate();
                            if (rowsInserted > 0) {
                                JOptionPane.showMessageDialog(frameTeacherGradeStudent.this, "Grade update Successful");
                            } else {
                                 JOptionPane.showMessageDialog(frameTeacherGradeStudent.this, "Grade update not Successful");
                            }
                        }
                    } else {
                         JOptionPane.showMessageDialog(frameTeacherGradeStudent.this, "Student not found in the database.");
                        //System.out.println("Student not found in the database.");
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

