    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        int deleteId = Integer.parseInt(tfDeleteTeacherID.getText());

        JFrame frame = new JFrame();
        int dialogResult = JOptionPane.showConfirmDialog(frame,
                "Do you want remove this account?", "Confirmation",
                JOptionPane.YES_NO_OPTION);

        if (dialogResult == JOptionPane.YES_OPTION) {

            try {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection connection = DriverManager
                        .getConnection(
                                "jdbc:sqlserver://localhost:1433;databaseName=RainbowPrimarySchool;selectMethod=cursor", "sa", "123456");

                // find if the username and password exist in database and also get the student id
                String sqlQuery = "DELETE FROM TeacherTable WHERE teacherId = ?";

                PreparedStatement statement = connection.prepareStatement(sqlQuery);

                statement.setInt(1, deleteId);

                int deleted = statement.executeUpdate();

                // Optionally, handle the result of the delete operation
                if (deleted > 0) {
                    JOptionPane.showMessageDialog(frameManageTeacherbyADMIN.this, deleteId + " is successfully deleted");
                } else {
                    JOptionPane.showMessageDialog(frameManageTeacherbyADMIN.this, deleteId + " is not deleted");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            frameManageTeacherbyADMIN manageTeacherbyADMIN = new frameManageTeacherbyADMIN();
            manageTeacherbyADMIN.setVisible(true);
            setVisible(false);
        } else {
            // User clicked "No," handle it as needed
            // JOptionPane.showMessageDialog(frame, "You clicked NO. Staying on the current page.");
        }


    }//GEN-LAST:event_jButton4ActionPerformed

