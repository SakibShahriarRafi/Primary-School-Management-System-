    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
        // TODO add your handling code here:
        int currentclass = Integer.parseInt(tfclass.getText());
        String section = tfSection.getText();
        String CourseID = tfCourseID.getText();
        int teacherID = Integer.parseInt(tfTeacheriD.getText());
        String jdbcUrl = "jdbc:sqlserver://localhost:1433;databaseName=RainbowPrimarySchool";
        String username = "sa";
        String password = "123456";
        
        try (Connection connection = DriverManager.getConnection(jdbcUrl, username, password)) {
            // Query the StudentTable to get student IDs matching the class and section
            String sqlSelect = "SELECT studentId FROM StudentTable WHERE studentCurentClass = ? AND studentSection = ?";
            try (PreparedStatement preparedStatement = connection.prepareStatement(sqlSelect)) {
                preparedStatement.setInt(1, currentclass);
                preparedStatement.setString(2, section);

                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    // Insert enrollment records for each student
                    String sqlInsert = "INSERT INTO EnrollmentTable (studentId, courseId, teacherId) VALUES (?, ?, ?)";
                    try (PreparedStatement insertStatement = connection.prepareStatement(sqlInsert)) {
                        while (resultSet.next()) {
                            int studentId = resultSet.getInt("studentId");
                            insertStatement.setInt(1, studentId);
                            insertStatement.setString(2, CourseID);
                            insertStatement.setInt(3, teacherID);
                            insertStatement.executeUpdate();
                        }

                        JOptionPane.showMessageDialog(frameManageCoursesbyADMIN.this, "Enrollment successful");
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEnrollActionPerformed

